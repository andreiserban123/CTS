package teme.pregatire.test1.s4.clase;

public class Pozitie {
    int x;
    int y;

    public Pozitie(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "Pozitie{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
